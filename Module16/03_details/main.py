shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700],
        ['обод', 500], ['шатун', 200], ['седло', 2700]]

print(shop)

incoming_part = input('введите деталь: ')
total_cost = 0
for line in shop:
    if line[0] == incoming_part:
        total_cost += line[1]
        print('найдена деталь', line[0])
print('Общая стоимость деталей', incoming_part, 'равна', total_cost)
